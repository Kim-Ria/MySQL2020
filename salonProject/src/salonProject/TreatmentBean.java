package salonProject;

public class TreatmentBean 
{
	private int t_no;
	private String treat_name;
	private int treat_price;
	
	public int getT_no() {
		return t_no;
	}
	public void setT_no(int t_no) {
		this.t_no = t_no;
	}
	public String getTreat_name() {
		return treat_name;
	}
	public void setTreat_name(String treat_name) {
		this.treat_name = treat_name;
	}
	public int getTreat_price() {
		return treat_price;
	}
	public void setTreat_price(int treat_price) {
		this.treat_price = treat_price;
	}
}
